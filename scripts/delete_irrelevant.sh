#!/bin/bash
cd /workspace/knowledge/原文库/

# 删除与K12 AI教育无关的文件
delete_files=(
  "10.1007_s00146-024-01864-9.md"
  "10.1007_s40692-023-00304-9.md"
  "10.1007_s41979-024-00121-4.md"
  "10.1007_s43681-024-00613-4.md"
  "10.1016_j.caeai.2025.100422.md"
  "10.1016_j_caeai_2025_100499.md"
  "10.1016_j_caeai_2026_100540.md"
  "10.1038_s41539-025_00398_z.md"
  "10.1038_s41539_026_00404_y.md"
  "10.1038_s41539_026_00410_0.md"
  "10.1080_07380569.2025.2602507.md"
  "10.1080_10494820.2025.2487538.md"
)

deleted=0
for f in "${delete_files[@]}"; do
  if [ -f "$f" ]; then
    rm -f "$f"
    echo "删除: $f"
    deleted=$((deleted+1))
  fi
done

echo "共删除: $deleted 个文件"
echo "---剩余文件数---"
ls *.md | wc -l
